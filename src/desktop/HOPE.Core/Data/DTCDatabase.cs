namespace HOPE.Core.Data;

/// <summary>
/// Comprehensive OBD2 Diagnostic Trouble Code database.
/// Contains 500+ standard codes with descriptions, causes, and repair information.
/// </summary>
public static class DTCDatabase
{
    /// <summary>
    /// Get DTC information by code.
    /// </summary>
    public static DTCInfo? GetInfo(string code)
    {
        if (string.IsNullOrEmpty(code))
            return null;

        code = code.ToUpper().Trim();
        return Database.TryGetValue(code, out var info) ? info : null;
    }

    /// <summary>
    /// Get description for a DTC code.
    /// </summary>
    public static string GetDescription(string code)
    {
        var info = GetInfo(code);
        return info?.Description ?? "Unknown fault code - consult vehicle service manual";
    }

    /// <summary>
    /// Get all codes in a category.
    /// </summary>
    public static IEnumerable<KeyValuePair<string, DTCInfo>> GetByCategory(DTCCategory category)
    {
        return Database.Where(kvp => kvp.Value.Category == category);
    }

    /// <summary>
    /// Search codes by keyword in description or causes.
    /// </summary>
    public static IEnumerable<KeyValuePair<string, DTCInfo>> Search(string keyword)
    {
        if (string.IsNullOrWhiteSpace(keyword))
            return Enumerable.Empty<KeyValuePair<string, DTCInfo>>();

        keyword = keyword.ToLower();
        return Database.Where(kvp =>
            kvp.Value.Description.ToLower().Contains(keyword) ||
            kvp.Value.PossibleCauses.Any(c => c.ToLower().Contains(keyword)));
    }

