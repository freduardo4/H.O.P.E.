/// <summary>
/// Information about a Diagnostic Trouble Code.
/// </summary>
public record DTCInfo(
    string Description,
    DTCCategory Category,
    DTCSeverity Severity,
    string[] PossibleCauses)
{
    /// <summary>
    /// Format possible causes as a single string.
    /// </summary>
    public string CausesText => string.Join("; ", PossibleCauses);
}

/// <summary>
/// DTC Categories based on SAE J2012 standard.
/// </summary>
public enum DTCCategory
{
    Powertrain,  // P codes
    Body,        // B codes
    Chassis,     // C codes
    Network      // U codes
}

/// <summary>
/// Severity levels for DTCs.
/// </summary>
public enum DTCSeverity
{
    Low,      // May not affect vehicle operation
    Medium,   // Should be addressed soon
    High,     // Requires prompt attention
    Critical  // Immediate attention required
}
